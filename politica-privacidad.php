      <p>
        Conservamos tus datos personales solo durante el tiempo necesario para
        cumplir con los fines descritos en esta política, a menos que la ley
        exija o permita un período de retención más largo.
      </p>

      <h2>6. Seguridad de los datos</h2>
      <p>
        Tomamos medidas técnicas y organizativas para proteger tus datos
        personales contra accesos no autorizados, pérdida, alteración o
        divulgación. Sin embargo, ninguna transmisión de datos por internet es
        completamente segura. Te recomendamos que protejas tus credenciales de
        acceso y te pongas en contacto con nosotros de inmediato si sospechas un
        uso indebido de tu información.
      </p>

