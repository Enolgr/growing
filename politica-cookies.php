      <h2>1. ¿Qué son las cookies?</h2>
      <p>
        Las cookies son pequeños archivos de texto que se almacenan en tu
        dispositivo (ordenador, tablet o móvil) cuando visitas un sitio web.
        Estas cookies permiten al sitio recordar tus acciones y preferencias
        (como el idioma, inicio de sesión, etc.) durante un período de tiempo,
        para que no tengas que volver a configurarlas cada vez que vuelvas a
        visitar la página.
      </p>

      <h2>2. Tipos de cookies que utilizamos</h2>
      <p>En nuestro sitio web utilizamos los siguientes tipos de cookies:</p>

      <h3>a. Cookies necesarias</h3>
      <p>
        Estas cookies son esenciales para que nuestra página web funcione
        correctamente. Sin ellas, no podrías navegar por el sitio ni utilizar
        algunas de sus funciones principales, como el acceso a áreas seguras.
      </p>

      <h3>b. Cookies de funcionalidad</h3>
      <p>
        Estas cookies nos permiten recordar tus preferencias (como tu idioma o
        región) para ofrecerte una experiencia más personalizada.
      </p>

      <h3>c. Cookies de análisis o rendimiento</h3>
      <p>
        Estas cookies recopilan información sobre cómo los usuarios interactúan
        con nuestro sitio web, como las páginas que visitan con más frecuencia o
        si reciben mensajes de error. Toda la información recogida es anónima y
        nos ayuda a mejorar el funcionamiento del sitio.
      </p>

      <h3>d. Cookies de publicidad y marketing</h3>
      <p>
        Estas cookies se utilizan para mostrar anuncios relevantes para ti, en
        función de tus intereses. También pueden limitar la cantidad de veces
        que ves un anuncio y medir la efectividad de nuestras campañas
        publicitarias.
      </p>

      <h2>3. ¿Cómo usamos las cookies?</h2>
      <p>Utilizamos las cookies para:</p>
      <ul>
        <li>Reconocer tu dispositivo y tus preferencias en futuras visitas.</li>
        <li>Analizar cómo interactúas con nuestro contenido para mejorarlo.</li>
        <li>
          Ofrecerte contenidos adaptados a tus intereses relacionados con
          nuestros cursos de plantas.
        </li>
        <li>Administrar campañas publicitarias y evaluar su rendimiento.</li>
      </ul>

      <h2>4. Terceros y cookies de terceros</h2>
      <p>
        En algunos casos, utilizamos servicios de terceros que también pueden
